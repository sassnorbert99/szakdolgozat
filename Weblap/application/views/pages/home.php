<h1>Varázstorony</h1>

<p><strong>Természettudományos Pályaorientációs és Módszertani Központ</strong></p>
<div class="page">
	<p>A <strong><a href="https://www.varazstorony.hu/">Varázstoronyban</a></strong> látogatható a Csillagászati Múzeum, a Planetárium, a Varázsterem, a Camera Obscura (periszkóp) és a Panoráma terasz (ahol előre meghirdetett időpontokban távcsöves bemutatókat szervezünk). Tanulói csoportoknak, óvodásoknak (előzetes bejelentkezéssel) múzeumpedagógiai foglalkozásokat, csillagászat, biológia, fizika, földrajz és kémia rendhagyó órákat tartunk.<br /><br /><a href="https://uni-eszterhazy.hu/public/pic/editor/45558e01a1d24063de06d8d06391ea7a_1920.jpg" rel="prettyPhoto[gallery1]"><img src="https://uni-eszterhazy.hu/public/pic/editor/45558e01a1d24063de06d8d06391ea7a.jpg" alt="" /></a></p>
<hr />
<h3><strong><br />NYITVATARTÁS</strong></h3>
<p>Február 1-től március 14-ig hétvégente 9:30-13:30-ig (13:00)<br />Március 15-től április 30-ig hétfő kivételével mindennap 9:30-15:30-ig (15:00)<br />Május 1-től augusztus 30-ig mindennap 9:30-17:30-ig (16:30)<br />Szeptember 1-től október 31-ig hétfő kivételével mindennap 9:30-15:30-ig (15:00)<br />November 1-től december 14-ig hétvégente 9:30-13:30-ig (13:00)<br />December 15-től január 31-ig zárva tartunk.<br /><br /><a href="https://uni-eszterhazy.hu/public/pic/editor/1113434a6017faff4f0b069338a6e497_1920.JPG" rel="prettyPhoto[gallery1]"><img src="https://uni-eszterhazy.hu/public/pic/editor/1113434a6017faff4f0b069338a6e497.JPG" alt="" /></a><br /><br />Csoportokat fogadunk a látogatási időn túl és hétköznapokon is, de előzetesen regisztrálniuk kell telefonon, vagy e-mailben.<br /><strong>Tel.:</strong> +36 36/520-400/2279 <br /><strong>E-mail:</strong> <a href="mailto:varazstorony@varazstorony.hu">varazstorony@varazstorony.hu</a>  <br />Pénzárnyitás minden esetben 9:00</p>
<hr />
<h3><strong><br />JEGYÁRAK</strong></h3>
<p>A Csillagászati Múzeum, a Varázsterem, a Camera Obscura (periszkóp) és a Panoráma terasz:<br />Diákoknak, nyugdíjasoknak (igazolvánnyal) 1000 Ft/fő.<br />Felnőtteknek: 1300 Ft/fő.<br />Családoknak (két felnőtt + két gyerek): 3000 Ft/család.<br />Sétajegy (panoráma terasz + egy választható terem): 500 Ft/fő<br />Múzeumpedagógiai foglalkozás: 800 Ft/fő<br />Rendhagyó óra: 800 Ft/fő<br />Planetáriumi műsor: diákoknak, nyugdíjasoknak: 800 Ft/fő, felnőtteknek 1200 Ft.<br />A Varázstorony látogatása ingyenes (beleértve a planetáriumi programokat is):<br />tanulócsoportokat kísérő pedagógusoknak (pedagógus igazolvánnyal),<br />iskoláskor alattiaknak és 70 év felettieknek,<br />az egyetem minden dolgozójának és közvetlen családtagjaiknak,<br />az egyetem hallgatóinak,<br />a gyakorlóiskola tanulóinak és tanárainak,<br />az egyetem hivatalos vendégeinek,<br />fogyatékkal élők és kísérőik számára.<br />További részletek <strong><a href="http://www.varazstorony.hu">itt érhetők el.</a></strong></p>
<hr />
<h3><br />RÖVID ISMERTETŐ A LÁTOGATHATÓ HELYEKRŐL</h3>
<p>(A Varázstorony a Líceum toronyépületének 4. szintjén [3. emeleten] kezdődik, csak lépcsőn közelíthető meg!)<br />Ezen a szinten női-férfi mosdók rendelkezésre állnak.</p>
<p><strong>Varázsterem</strong> (4. szint)<br />A tudomány széleskörű megértését a „kézzel fogható", kipróbálva tanulás nagyban elősegíti. A Varázsterembe ellátogatók érdekes, saját maguk által elvégezhető kísérleteken keresztül ismerkedhetnek meg alapvető fizikai jelenségekkel. Tudományos „játékokat" játszhatnak. Számítógépen természeti jelenségek eredeti felvételeit, animációit futtathatják le. Lehetőség nyílik az Interneten természettudományos csatornák, honlapok böngészésére is.</p>
<p>A teremben az interaktív kísérletezésen túl a látogatók régi kísérleti eszközökből, és a Bükk hegységből származó kőzetekből, ősmaradványokból álló kiállítást szemlélhetnek meg.<br /><br /><a href="https://uni-eszterhazy.hu/public/pic/editor/e107a0415e869aab5a3e78c0edaddc9d_1920.JPG" rel="prettyPhoto[gallery1]"><img src="https://uni-eszterhazy.hu/public/pic/editor/e107a0415e869aab5a3e78c0edaddc9d.JPG" alt="" /></a></p>
<p><strong>Hell Miksa Experimentárium</strong> (Rendhagyó órák terme) (4. szint)<br />Az Eszterházy Károly Egyetem Természettudományi Karán a kar tanárai diákcsoportoknak rendhagyó biológia, fizika, földrajz és kémia órákat tartanak, melyek szervesen kapcsolódnak a Varázstorony programjához, s az Egerbe érkező iskolás csoportok igényelhetik. Ezen kísérleti bemutatókkal, magas színvonalú szemléltetéssel ötvözött foglalkozásokat előre egyeztetett témákban és időpontokban tartják. <br /><br />Előzetes egyeztetés legalább egy héttel a látogatás előtt a 36/520-400/2279, vagy: <a href="mailto:varazstorony@varazstorony.hu">varazstorony@varazstorony.hu<br /><br /></a><a href="https://uni-eszterhazy.hu/public/pic/editor/f77cae7d9e4447d26b9806d0b9f84edd_1920.JPG" rel="prettyPhoto[gallery1]"><img src="https://uni-eszterhazy.hu/public/pic/editor/f77cae7d9e4447d26b9806d0b9f84edd.JPG" alt="" /></a></p>
<p><strong>Csillagászati Múzeum</strong> (7. szint)<br />A Csillagásztorony – eredeti nevén a Specula – 1776-ban kezdte el működését. Az obszervatórium korának legjobb csillagászati műszereivel volt felszerelve. Ezeket Hell Miksa magyar születésű, bécsi császári, királyi udvari csillagász, az akkori idők legjobb bécsi és londoni távcsőkészítő mestereitől rendelte meg. Az eredeti műszerek kiállítása tekinthető meg a Csillagászati Múzeumban. (Csillagászattal kapcsolatos kiállítások a felsőbb emeleteken is találhatók.)<br />Csoportok részére múzeumpedagógiai foglalkozásokat is tartunk. </p>
<p>Előzetes egyeztetés: 36/520-400/2279, vagy: <a href="mailto:varazstorony@varazstorony.hu">varazstorony@varazstorony.hu<br /><br /></a><a href="https://uni-eszterhazy.hu/public/pic/editor/18ddca519a99bf98337b25f2fb0309db_1920.jpg" rel="prettyPhoto[gallery1]"><img src="https://uni-eszterhazy.hu/public/pic/editor/18ddca519a99bf98337b25f2fb0309db.jpg" alt="" /></a></p>
<p><strong>Planetárium</strong> (7. szint)<br />Magyarország jelenleg harmadik működő planetáriuma. A 6 méter átmérőjű, félgömb alakú, közel 60 négyzetméteres vetítőfelületen az éjszakai csillagos égbolt valósághű képmása jelenik meg. A csillagos eget, a Naprendszer bolygóit és azok mozgását bemutató programok nagy érdeklődést válthatnak ki, mert az iskolai fizika és földrajz tantervekben szereplő csillagászattal kapcsolatos tananyagokat látványosan és élményszerűen közvetítik. <br /><br />Előzetes egyeztetés: 36/520-400/2279, vagy: <a href="mailto:varazstorony@varazstorony.hu">varazstorony@varazstorony.hu</a></p>
<p><strong>Camera Obscura</strong> (Sötétkamra, periszkóp 10. szint)<br />Az 1776-ban készült, Hell Miksa által tervezett berendezés nem csak Magyarországon, hanem az egész világon ritkaságnak számít. Optikái segítségével a barokk Eger és környéke vetíthető ki a besötétített terem közepén lévő asztalra.<br /><br /><a href="https://uni-eszterhazy.hu/public/pic/editor/aa6e8947f28160d667ce7e31a2b7c3c8_1920.jpg" rel="prettyPhoto[gallery1]"><img src="https://uni-eszterhazy.hu/public/pic/editor/aa6e8947f28160d667ce7e31a2b7c3c8.jpg" alt="" /></a></p>
<p><strong>Panoráma Terasz</strong> (9. szint)<br />A fenti érdekességek mellett szép kilátásban lehet részük, ha kilépnek a torony teraszára. Madártávlatból csodálhatják meg a várost.</p>
<p><a href="https://uni-eszterhazy.hu/public/pic/editor/7d50f04021ba6d64d046581557efe5dc_1920.JPG" rel="prettyPhoto[gallery1]"><img src="https://uni-eszterhazy.hu/public/pic/editor/7d50f04021ba6d64d046581557efe5dc.JPG" alt="" /></a></p>
