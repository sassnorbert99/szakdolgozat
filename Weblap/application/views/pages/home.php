<h3>LÁTOGATÁSI REND</h3>
<p>Csoportos látogatás esetén, érkezési rendben követhetik egymást a csoportok, egy csoport létszáma 25-30 fő lehet (kivéve, mint lentebb látható a Camera Obscura és az Experimentárium), nagyobb csoport esetén csoportbontást kell végezni.</p>
<p>A Camera Obscura befogadóképessége 15-20 fő.<br />A Rendhagyó óra termében 35-40 férőhely van.<br />Előzetes bejelentkezés esetén az igényelt időpontban biztosítjuk a csoport látogatását. Bejelentkezés a +36 36/520-400/2279 telefonszámon, vagy: <a href="mailto:varazstorony@varazstorony.hu">varazstorony@varazstorony.hu</a> <br />A Varázstoronyban a látogatási idő kb. 60-70 perc.<br />Ezen túl egy planetáriumi műsor, vagy egy rendhagyó óra még 45-50 perc.<br />Különleges esetekben a fentiektől el lehet térni.</p>
<p>Felmerülő egyéb kérdéseikre az alábbi elérhetőségeken kaphatnak választ: <br />E-mail: <a href="mailto:varazstorony@varazstorony.hu">varazstorony@varazstorony.hu</a>  Tel.: +36 36/520-400/2279</p>
